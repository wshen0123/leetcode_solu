from functools import reduce

class Solution:
    # @param {integer[]} nums
    # @return {integer}
    def maxProduct(self, nums):
        def mult(nums):
            if not nums:
                return 0
            return reduce(lambda x, y:x*y, nums)
        
        def maxProductNoZero(nums):
            neg_map = [1 if i < 0 else 0 for i in nums]
            neg_num = sum(neg_map)
            if neg_num == 0 or neg_num % 2 == 0:
                return mult(nums)
            else:
                firstNeg = neg_map.index(1)
                lastNeg = neg_map[::-1].index(1)
                if firstNeg == lastNeg:
                    return max(mult(nums[:firstNeg]), mult(nums[lastNeg + 1:]))
                else:
                    mid_mult = mult(nums[firstNeg + 1:lastNeg])
                    return max(mult(nums[:firstNeg + 1]) * mid_mult, mid_mult * mult(nums[lastNeg:]))

        print('begin')
        begin = 0
        end = 0
        m = 0
        while begin < len(nums) - 1:
            end = nums[begin+1:].index(0)
            m = max(maxProductNoZero(nums[begin:end]), m)
            begin = end

        print('return')
        return m
        
